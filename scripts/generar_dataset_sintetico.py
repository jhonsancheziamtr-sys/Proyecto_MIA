print('  1. Subir el CSV a Google Drive en /MyDrive/Proyecto_Swarovski/')
print("  2. En BLOQUE 1 del pipeline: FILE_DATASET = 'Dataset_Sintetico_Joyeria.csv'")
print('  3. Ejecutar todo el pipeline')

